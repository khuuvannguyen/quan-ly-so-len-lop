    private void FillData(List<Monhoc> list) {
        defaultTableModel.setRowCount(0);
        for (Monhoc monhoc : list) {
            defaultTableModel.addRow(new Object[]{
                monhoc.getMaMon(),
                monhoc.getTenMon(),
                monhoc.getHocKy(),
                monhoc.getLyThuyet(),
                monhoc.getThucHanh()
            });
        }
    }

