    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (txtMaMH.getText().trim().isEmpty()) {
            ShowError("Vui lòng nhập mã môn học");
        } else {
            String maMH = txtMaMH.getText();
            String reg = "^\\w{2}\\d{3}$";
            if (!maMH.matches(reg)) {
                ShowError("Mã môn học không đúng định dạng\n"
                        + "Ví dụ: MH123");
            } else {
                if (monhocServices.DuplicationMonHoc(maMH)) {
                    ShowError("Mã môn học đã tồn tại");
                } else {
                    if (!txtTenMH.getText().trim().isEmpty()) {
                        String tenMH = txtTenMH.getText();
                        int lyThuyet = 0;
                        int thucHanh = 0;
                        try {
                            lyThuyet = Integer.parseUnsignedInt(String.valueOf(spinnerLyThuyet.getValue()));
                            try {
                                thucHanh = Integer.parseUnsignedInt(String.valueOf(spinnerThucHanh.getValue()));
                                int hocKy = Integer.parseInt(String.valueOf(spinnerHocKy.getValue()));
                                if (lyThuyet != 0 && thucHanh != 0) {
                                    Monhoc monhoc = new Monhoc(maMH, tenMH, hocKy, lyThuyet, thucHanh);
                                    monhocServices.addMonhoc(monhoc);
                                    FillData(monhocServices.getMonhoc());
                                } else {
                                    ShowError("Vui lòng chọn số tiết");
                                }
                            } catch (Exception e) {
                                ShowError("Tổng số tiết thực hành không hợp lệ");
                            }
                        } catch (Exception e) {
                            ShowError("Tổng số tiết lý thuyết không hợp lệ");
                        }
                    }else{
                        ShowError("Vui lòng nhập tên môn học");
                    }
                }
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

