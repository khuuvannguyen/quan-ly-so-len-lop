    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:

        maGVB = comboMaGV.getSelectedItem().toString();
        String stringNH = comboNamHoc.getSelectedItem().toString();
        namHocB = stringNH;
        maGVA = maGVB.substring(2);
        namHocA = stringNH.substring(2, 4) + stringNH.substring(7);
        maSo = "S" + namHocA + maGVA;
        txtMaSo.setText(maSo);

    }//GEN-LAST:event_btnCreateActionPerformed

