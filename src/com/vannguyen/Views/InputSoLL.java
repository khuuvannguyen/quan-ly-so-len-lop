    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (txtMaSo.getText().trim().isEmpty()) {
            ShowError("Vui lòng tạo mã sổ");
        } else {
            if (soLLServices.DuplicateSoLL(maSo)) {
                ShowError("Mã sổ đã tồn tại");
            } else {
                String maDV = NewDao.getDonviOfGiaovien(maGVB);
                SoLL soLL = new SoLL(maSo, maGVB, maDV, namHocB);
                soLLServices.addSoLL(soLL);
                FillData(soLLServices.getSoLL());
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnReLoadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReLoadActionPerformed
        // TODO add your handling code here:
        FillData(soLLServices.getSoLL());
        comboNamHoc.removeAllItems();
        LoadComboBox();
        comboMaGV.removeAllItems();
        loadMaGV();
    }//GEN-LAST:event_btnReLoadActionPerformed

