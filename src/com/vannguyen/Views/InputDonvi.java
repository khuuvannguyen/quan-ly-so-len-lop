    private void FillData(List<Donvi> list) {
        defaultTableModel.setRowCount(0);
        for (Donvi donvi : list) {
            defaultTableModel.addRow(new Object[]{
                donvi.getMaDonvi(),
                donvi.getTenDonvi()
            });
        }
    }

