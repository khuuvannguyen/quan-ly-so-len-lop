    private void ShowError(String detail) {
        JOptionPane.showMessageDialog(this, detail, "Đã xảy ra lỗi", JOptionPane.ERROR_MESSAGE);
    }
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (txtMaDonvi.getText().trim().isEmpty()) {
            ShowError("Vui lòng điền mã đơn vị");
        } else {
            String maDonvi = txtMaDonvi.getText();
            if (donviServices.DuplicateMaDonvi(maDonvi)) {
                ShowError("Mã đơn vị đã tồn tại");
            } else {
                if (!txtTenDonvi.getText().trim().isEmpty()) {
                    String tenDonvi = txtTenDonvi.getText();
                    Donvi donvi = new Donvi(maDonvi, tenDonvi);
                    donviServices.addDonvi(donvi);
                    FillData(donviServices.getDonvi());
                } else {
                    ShowError("Vui lòng nhập tên đơn vị");
                }
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

