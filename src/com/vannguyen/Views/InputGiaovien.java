    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (txtMaGiaovien.getText().trim().isEmpty()) {
            ShowError("Vui lòng nhập mã giáo viên");
        } else {
            String maGiaovien = txtMaGiaovien.getText();
            String reg = "^\\w{2}\\d{3}$";
            if (!maGiaovien.matches(reg)) {
                ShowError("Mã giáo viên không đúng định dạng"
                        + "\nVí dụ: GV123");
            } else {
                if (giaovienServices.DuplicateGiaovien(maGiaovien)) {
                    ShowError("Mã giáo viên đã tồn tại");
                } else {
                    if (!txtHoten.getText().trim().isEmpty()) {
                        String hoTen = txtHoten.getText();
                        String gioiTinh = "";
                        String namSinh = txtNamsinh.getText();
                        if (radioNam.isSelected()) {
                            gioiTinh += "Nam";
                        } else if (radioNu.isSelected()) {
                            gioiTinh += "Nữ";
                        } else if (radioKhac.isSelected()) {
                            gioiTinh += "Khác";
                        }
                        //Xử lý combo box
                        int index = comboBox.getSelectedIndex();
                        String selectedText = comboBox.getItemAt(index);
                        String maDonvi = giaovienServices.TakeMaDV(selectedText);
                        //--------------------
                        Giaovien giaovien = new Giaovien(maGiaovien, hoTen, gioiTinh, namSinh, maDonvi);
                        giaovienServices.addGiaovien(giaovien);
                        try {
                            LoginDao.addLogin(maGiaovien, "123", "giaovien");
                        } catch (Exception e) {
                        }
                        defaultTableModel.setRowCount(0);
                        FillData(giaovienServices.getGiaovien());
                    } else {
                        ShowError("Vui lòng nhập tên giáo viên");
                    }
                }
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

