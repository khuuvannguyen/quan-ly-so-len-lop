    private void FillData(List<Giaovien> list) {
        for (Giaovien giaovien : list) {
            defaultTableModel.addRow(new Object[]{
                giaovien.getMaGiaovien(),
                giaovien.getHoTen(),
                giaovien.getGioiTinh(),
                giaovien.getNamSinh(),
                giaovien.getMaDonvi()
            });
        }
    }

    private void LoadComboBox() {
        ArrayList<String> list = giaovienServices.LoadComboBox();
        for (String item : list) {
            comboBox.addItem(item);
        }
    }

